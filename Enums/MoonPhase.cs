namespace EphemSharp.Enums
{
    public enum MoonPhase
    {
        NewMoon,         // Новолуние (0°)
        WaxingCrescent,  // Растущий серп (45°)
        FirstQuarter,    // Первая четверть (90°)
        WaxingGibbous,   // Растущая Луна (135°)
        FullMoon,        // Полнолуние (180°)
        WaningGibbous,   // Убывающая Луна (225°)
        ThirdQuarter,    // Последняя четверть (270°)
        WaningCrescent   // Убывающий серп (315°)
    }
}
